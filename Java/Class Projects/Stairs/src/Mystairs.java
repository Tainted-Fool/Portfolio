public class Mystairs {
	public static final int STEPS = 1;
		public static void main(String[] args) {
			
			for (int i = 1; i <= STEPS; i++){
				System.out.print("     ");
			}
			
			System.out.print("  O  ******");
			
			for (int i = 1; i < STEPS; i++){
				System.out.print("     ");
			}
			
			System.out.println("*");
			
			for (int i = 1; i <= STEPS; i++){
				System.out.print("     ");
			}
			
			System.out.print(" /|\\ *     ");
			
			for (int i = 1; i < STEPS; i++){
				System.out.print("     ");
			}
			
			System.out.println("*");
			
			for (int i = 1; i <= STEPS; i++){
				System.out.print("     ");
			}
			
			System.out.print(" / \\ *     ");
			
			for (int i = 1; i < STEPS; i++){
				System.out.print("     ");
			}
			
			System.out.println("*");
			}
		}

